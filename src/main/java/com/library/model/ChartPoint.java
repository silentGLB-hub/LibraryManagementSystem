package com.library.model;

public class ChartPoint {

    private String label;
    private int barValue;
    private int lineValue;
    private int barPercent;
    private int barY;
    private int barHeight;
    private int x;
    private int y;

    public String getLabel() {
        return label;
    }

    public void setLabel(String label) {
        this.label = label;
    }

    public int getBarValue() {
        return barValue;
    }

    public void setBarValue(int barValue) {
        this.barValue = barValue;
    }

    public int getLineValue() {
        return lineValue;
    }

    public void setLineValue(int lineValue) {
        this.lineValue = lineValue;
    }

    public int getBarPercent() {
        return barPercent;
    }

    public void setBarPercent(int barPercent) {
        this.barPercent = barPercent;
    }

    public int getBarY() {
        return barY;
    }

    public void setBarY(int barY) {
        this.barY = barY;
    }

    public int getBarHeight() {
        return barHeight;
    }

    public void setBarHeight(int barHeight) {
        this.barHeight = barHeight;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}
